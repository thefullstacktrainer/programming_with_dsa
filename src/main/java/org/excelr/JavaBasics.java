package org.excelr;

import java.util.Scanner;

public class JavaBasics {
        public static void main(String[] args) {

                System.out.println("Hello Excelr!");

                // Declarations and Assignments:
                int student_age; // Declaration
                student_age = 25; // Assignment
                System.out.println(student_age);

                // Variables in Java are declared with a type and can hold different values.
                int count = 0;
                String message = "Hello, World!";

                // Syntax : primitive Data Types
                // int: Used for representing integers (whole numbers).
                int age = 25;

                // double: Used for representing floating-point numbers (decimal numbers).
                double salary = 50000.50;
                //
                // float: Similar to double but with less precision. It is less commonly used.
                float temperature = 98.6f; // Note: 'f' is used to indicate a float literal
                //
                // char: Used for representing a single character.
                char grade = 'A';

                // boolean: Used for representing true or false values.
                boolean isJavaFun = true;
                //
                // byte: Used for representing small integer values.
                byte byteValue = 120;
                //
                // short: Used for representing short integer values.
                short shortValue = 30000;

                // long: Used for representing long integer values.
                long bigNumber = 123456789012345L; // Note: 'L' is used to indicate a long literal

                // Reference Data Types:
                // Reference data types are more complex and are used to store references to
                // objects.

                // String: Used for representing sequences of characters (text).
                String message2 = "Hello, Java!";

                // Arrays: Used for storing collections of elements of the same type.
                int[] numbers = { 1, 2, 3, 4, 5 };

                // Default Values:
                int number; // Defaults to 0
                boolean flag; // Defaults to false

                // This is a single-line comment

                /*
                 * This is a multi-line comment
                 * spanning multiple lines.
                 */
        }

}
